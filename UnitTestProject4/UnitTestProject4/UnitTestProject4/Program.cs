using System;

namespace UnitTestProject4
{
 class Program
    {
        public double Add(double v1, double v2)
        {
            return v1 + v2;
        }
        public double Sub(double v1, double v2)
        {
            return v1 - v2;
        }
        public double Div(double v1, double v2)
        {
            return v1 / v2;
        }
        public double Mul(double v1, double v2)
        {
            return v1 * v2;
        }
    }
}